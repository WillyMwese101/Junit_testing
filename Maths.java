public class Maths {



    public int add(int a, int b) {
        int sum = a + b;
        return sum;
    }
    
    public String add(String a, String b){
        String ans = a + b;
        return ans;
    }

    public int subtract(int a, int b) {
        int sub = a - b;
        return sub;
    }

    public int product(int a, int b) {
        int prod = a*b;
        return prod;
    }

    public int divide(int a, int b) {
        int div = a/b;
        return div;
    }
}
